    private void ucitajSve() {
        try {
            List<StrucnaSprema> lista = ClientController.getInstance().vratiSveStrucneSpreme();
            tableModel.setLista(lista);
        } catch (Exception e) {
            tableModel.setLista(new ArrayList<>());
            javax.swing.JOptionPane.showMessageDialog(this, "Greška pri učitavanju: " + e.getMessage());
        }
    }

    private void popuniPoljaIzTabele() {
        int row = tblSprema.getSelectedRow();
        if (row < 0) return;
        StrucnaSprema ss = tableModel.getStrucnaSpremaAt(row);
        txtNaziv.setText(ss.getNazivSpreme());
        cmbStepen.setSelectedItem(ss.getStepen());
    }

    private void ocisti() {
        txtNaziv.setText("");
        cmbStepen.setSelectedIndex(0);
        tblSprema.clearSelection();
    }

