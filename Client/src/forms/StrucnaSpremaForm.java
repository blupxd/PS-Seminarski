    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        int selectedRow = tblSprema.getSelectedRow();
        if (selectedRow < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Odaberite stavku iz tabele za izmenu.");
            return;
        }
        String naziv = txtNaziv.getText().trim();
        String stepen = (String) cmbStepen.getSelectedItem();
        if (naziv.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Unesite naziv stručne spreme.");
            return;
        }
        try {
            StrucnaSprema ss = new StrucnaSprema();
            ss.setIdStrucnaSprema(tableModel.getStrucnaSpremaAt(selectedRow).getIdStrucnaSprema());
            ss.setNazivSpreme(naziv);
            ss.setStepen(stepen);
            ClientController.getInstance().promeniStrucnuSpremu(ss);
            ucitajSve(); ocisti();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage());
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selectedRow = tblSprema.getSelectedRow();
        if (selectedRow < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Odaberite stavku iz tabele za brisanje.");
            return;
        }
        int conf = javax.swing.JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite obrisati odabranu stavku?", "Potvrda brisanja", javax.swing.JOptionPane.YES_NO_OPTION);
        if (conf != javax.swing.JOptionPane.YES_OPTION) return;
        try {
            StrucnaSprema ss = tableModel.getStrucnaSpremaAt(selectedRow);
            ClientController.getInstance().obrisiStrucnuSpremu(ss);
            ucitajSve(); ocisti();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        String naziv = txtNaziv.getText().trim();
        String stepen = (String) cmbStepen.getSelectedItem();
        if (naziv.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Unesite naziv stručne spreme.");
            return;
        }
        try {
            StrucnaSprema ss = new StrucnaSprema();
            ss.setNazivSpreme(naziv);
            ss.setStepen(stepen);
            ClientController.getInstance().kreirajStrucnuSpremu(ss);
            javax.swing.JOptionPane.showMessageDialog(this, "Систем је запамтио струčну спрему.");
            ucitajSve();
            ocisti();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Систем не може да запамти стручну спрему");
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnOcistiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOcistiActionPerformed
        ocisti();
        ucitajSve();
    }//GEN-LAST:event_btnOcistiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        try {
            StrucnaSprema kriterijum = new StrucnaSprema();
            kriterijum.setNazivSpreme(txtNaziv.getText().trim());
            String stepen = (String) cmbStepen.getSelectedItem();
            kriterijum.setStepen(stepen != null && !stepen.isEmpty() ? stepen : null);
            List<StrucnaSprema> lista = ClientController.getInstance().pretrazujStrucneSpreme(kriterijum);
            tableModel.setLista(lista);
        } catch (Exception e) {
            tableModel.setLista(new ArrayList<>());
            javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage());
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

