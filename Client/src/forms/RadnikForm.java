    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        Radnik r = kreirajIzPolja();
        if (r == null) return;
        if (new String(txtSifra.getPassword()).trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Šifra je obavezna pri dodavanju.");
            return;
        }
        try {
            ClientController.getInstance().kreirajRadnika(r);
            ucitajSve(); ocisti();
        } catch (Exception e) { javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage()); }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnOcistiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOcistiActionPerformed
        ocisti();
        ucitajSve();
    }//GEN-LAST:event_btnOcistiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        try {
            Radnik kriterijum = new Radnik();
            kriterijum.setIme(txtIme.getText().trim());
            kriterijum.setPrezime(txtPrezime.getText().trim());
            kriterijum.setKorisnickoIme(txtKorisnickoIme.getText().trim());
            kriterijum.setEmail(txtEmail.getText().trim());
            List<Radnik> lista = ClientController.getInstance().pretrazujRadnike(kriterijum);
            tableModel.setLista(lista);
        } catch (Exception e) {
            tableModel.setLista(new ArrayList<>());
            javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage());
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnPromeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromeniActionPerformed
        int row = tblRadnici.getSelectedRow();
        if (row < 0) { javax.swing.JOptionPane.showMessageDialog(this, "Odaberite radnika iz tabele."); return; }
        Radnik r = kreirajIzPolja();
        if (r == null) return;
        r.setIdRadnik(tableModel.getRadnikAt(row).getIdRadnik());
        try {
            ClientController.getInstance().promeniRadnika(r);
            ucitajSve(); ocisti();
        } catch (Exception e) { javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage()); }
    }//GEN-LAST:event_btnPromeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int row = tblRadnici.getSelectedRow();
        if (row < 0) { javax.swing.JOptionPane.showMessageDialog(this, "Odaberite radnika iz tabele."); return; }
        int potvrda = javax.swing.JOptionPane.showConfirmDialog(this, "Brisanje radnika je nepovratno. Nastavi?", "Potvrda", javax.swing.JOptionPane.YES_NO_OPTION);
        if (potvrda != javax.swing.JOptionPane.YES_OPTION) return;
        int id = tableModel.getRadnikAt(row).getIdRadnik();
        try {
            ClientController.getInstance().obrisiRadnika(id);
            ucitajSve(); ocisti();
        } catch (Exception e) { javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage()); }
    }//GEN-LAST:event_btnObrisiActionPerformed

