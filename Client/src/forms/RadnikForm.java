    private void ucitajSve() {
        try {
            List<Radnik> lista = ClientController.getInstance().vratiSveRadnike();
            tableModel.setLista(lista);
        } catch (Exception e) {
            tableModel.setLista(new ArrayList<>());
            javax.swing.JOptionPane.showMessageDialog(this, "Greška pri učitavanju: " + e.getMessage());
        }
    }

    private void popuniPoljaIzTabele() {
        int row = tblRadnici.getSelectedRow();
        if (row < 0) return;
        Radnik r = tableModel.getRadnikAt(row);
        txtIme.setText(r.getIme());
        txtPrezime.setText(r.getPrezime());
        txtKorisnickoIme.setText(r.getKorisnickoIme());
        txtSifra.setText("");
        txtEmail.setText(r.getEmail());
    }

    private Radnik kreirajIzPolja() {
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        String korisnickoIme = txtKorisnickoIme.getText().trim();
        String sifra = new String(txtSifra.getPassword()).trim();
        String email = txtEmail.getText().trim();
        if (ime.isEmpty() || prezime.isEmpty() || korisnickoIme.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ime, prezime i korisničko ime su obavezni.");
            return null;
        }
        Radnik r = new Radnik();
        r.setIme(ime);
        r.setPrezime(prezime);
        r.setKorisnickoIme(korisnickoIme);
        r.setSifra(sifra.isEmpty() ? null : sifra);
        r.setEmail(email);
        return r;
    }

    private void ocisti() {
        txtIme.setText("");
        txtPrezime.setText("");
        txtKorisnickoIme.setText("");
        txtSifra.setText("");
        txtEmail.setText("");
        tblRadnici.clearSelection();
    }

