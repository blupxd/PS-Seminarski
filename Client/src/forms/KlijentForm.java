    private void ucitajSve() {
        try {
            List<Klijent> lista = ClientController.getInstance().vratiSveKlijente();
            tableModel.setLista(lista);
        } catch (Exception e) {
            tableModel.setLista(new ArrayList<>());
            javax.swing.JOptionPane.showMessageDialog(this, "Greška pri učitavanju: " + e.getMessage());
        }
    }

    private void popuniPoljaIzTabele() {
        int row = tblKlijenti.getSelectedRow();
        if (row < 0) {
            return;
        }
        Klijent k = tableModel.getKlijentAt(row);
        try {
            Klijent found = (Klijent) ClientController.getInstance().sendOperation(
                new Operation(OperationType.PRETRAZI_KLIJENT, k.getIdKlijent())
            ).getResponse();
            txtAdresa.setText(found.getAdresa() != null ? found.getAdresa() : "");
            txtTelefon.setText(found.getTelefon() != null ? found.getTelefon() : "");
            txtEmail.setText(found.getEmail() != null ? found.getEmail() : "");
            javax.swing.JOptionPane.showMessageDialog(this, "Систем је нашао клијента");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Систем не може да нађе клијента");
        }
    }


    private void ocisti() {
        txtAdresa.setText("");
        txtTelefon.setText("");
        txtEmail.setText("");
        tblKlijenti.clearSelection();
    }

