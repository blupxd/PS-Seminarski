    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        try {
            Klijent kriterijum = new Klijent();
            kriterijum.setAdresa(txtAdresa.getText().trim());
            kriterijum.setTelefon(txtTelefon.getText().trim());
            kriterijum.setEmail(txtEmail.getText().trim());
            List<Klijent> lista = ClientController.getInstance().vratiKlijentePo(kriterijum);
            tableModel.setLista(lista);
            if (lista != null && !lista.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Систем је нашао клијенте по задатим критеријумима");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Систем не може да нађе клијенте по задатим критеријумима");
            }
        } catch (Exception e) {
            tableModel.setLista(new ArrayList<>());
            javax.swing.JOptionPane.showMessageDialog(this, "Систем не може да нађе клијенте по задатим критеријумима");
        }    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnPromeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromeniActionPerformed
        int row = tblKlijenti.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Odaberite klijenta iz tabele.");
            return;
        }
        Klijent k = tableModel.getKlijentAt(row);
        KlijentUpravljanjeForm forma = new KlijentUpravljanjeForm(k);
        forma.setVisible(true);
        forma.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                if (forma.isSacuvano()) {
                    ucitajSve();
                }
            }
        });    }//GEN-LAST:event_btnPromeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int row = tblKlijenti.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Odaberite klijenta iz tabele.");
            return;
        }
        int potvrda = javax.swing.JOptionPane.showConfirmDialog(this, "Brisanje klijenta je nepovratno. Nastavi?", "Potvrda", javax.swing.JOptionPane.YES_NO_OPTION);
        if (potvrda != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }
        int id = tableModel.getKlijentAt(row).getIdKlijent();
        try {
            ClientController.getInstance().obrisiKlijenta(id);
            javax.swing.JOptionPane.showMessageDialog(this, "Систем је обрисао клијента.");
            ucitajSve();
            ocisti();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Систем не може да обрише клијента");
        }    }//GEN-LAST:event_btnObrisiActionPerformed

